package com.application.facebook.model;

public class FBModel {
	
	protected String id;
	protected String name;
	
	public FBModel(){}
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}